#include "ivideoconfig.h"
#include "ui_ivideoconfig.h"

/*****************************************************************************
 *
 *****************************************************************************/
IVideoConfig::IVideoConfig(QWidget *parent) :
    QDialog(parent),
    ui(new Ui::IVideoConfig)
{
    ui->setupUi(this);
    vOkClicked = false;

}

/*****************************************************************************
 *
 *****************************************************************************/
IVideoConfig::~IVideoConfig()
{
    delete ui;
}

/*****************************************************************************
 *
 *****************************************************************************/
QString IVideoConfig::getFileName()
{
    return ui->filLocation->getFileName();
}

/*****************************************************************************
 *
 *****************************************************************************/
bool IVideoConfig::OkClicked()
{
    return vOkClicked;
}

/*****************************************************************************
 *
 *****************************************************************************/
void IVideoConfig::setFileName(QString val)
{
    ui->filLocation->setFileName(val);
}

/*****************************************************************************
 *
 *****************************************************************************/
void IVideoConfig::on_cmdOk_clicked()
{
    vOkClicked = true;
    this->close();
}

/*****************************************************************************
 *
 *****************************************************************************/
void IVideoConfig::on_cmdCancel_clicked()
{
    vOkClicked = false;
    this->close();
}
