#include "icameradialog.h"
#include "ui_icameradialog.h"

/*****************************************************************************
 *
 *****************************************************************************/
ICameraDialog::ICameraDialog(QWidget *parent) :
    QDialog(parent),
    ui(new Ui::ICameraDialog)
{
    ui->setupUi(this);
    vOkClicked = false;
    cameraId = 0;
}

/*****************************************************************************
 *
 *****************************************************************************/
ICameraDialog::~ICameraDialog()
{
    delete ui;
}

/*****************************************************************************
 *
 *****************************************************************************/
int ICameraDialog::getCameraId()
{
    return cameraId;
}

/*****************************************************************************
 *
 *****************************************************************************/
bool ICameraDialog::OkClicked()
{
    return vOkClicked;
}

/*****************************************************************************
 *
 *****************************************************************************/
void ICameraDialog::setCameraId(int id)
{
    cameraId = id;
}

/*****************************************************************************
 *
 *****************************************************************************/
void ICameraDialog::showEvent(QShowEvent*)
{
    ui->spnCamera->setValue(cameraId);
    vOkClicked = false;
}

/*****************************************************************************
 *
 *****************************************************************************/
void ICameraDialog::on_cmdOk_clicked()
{
    cameraId = ui->spnCamera->value();
    vOkClicked = true;
    close();
}

/*****************************************************************************
 *
 *****************************************************************************/
void ICameraDialog::on_cmdCancel_clicked()
{
    ui->spnCamera->setValue(cameraId);
    vOkClicked = false;
    close();
}
