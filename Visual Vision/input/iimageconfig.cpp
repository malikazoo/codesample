#include "iimageconfig.h"
#include "ui_iimageconfig.h"

/*****************************************************************************
 *
 *****************************************************************************/
IImageConfig::IImageConfig(QWidget *parent) :
    QDialog(parent),
    ui(new Ui::IImageConfig)
{
    ui->setupUi(this);
    vOkClicked = false;
}

/*****************************************************************************
 *
 *****************************************************************************/
IImageConfig::~IImageConfig()
{
    delete ui;
}

/*****************************************************************************
 *
 *****************************************************************************/
QString IImageConfig::getFileName()
{
    return ui->filBrowser->getFileName();
}

/*****************************************************************************
 *
 *****************************************************************************/
bool IImageConfig::getOkClicked()
{
    return vOkClicked;
}

void IImageConfig::setFileName(QString name)
{
    ui->filBrowser->setFileName(name);
}

/*****************************************************************************
 *
 *****************************************************************************/
void IImageConfig::on_cmdOk_clicked()
{
    vOkClicked = true;
    close();
}

/*****************************************************************************
 *
 *****************************************************************************/
void IImageConfig::on_cmdCancel_clicked()
{
    vOkClicked = false;
    close();
}
