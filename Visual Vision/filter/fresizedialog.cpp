#include "fresizedialog.h"
#include "ui_fresizedialog.h"

/*****************************************************************************
 *
 *****************************************************************************/
FResizeDialog::FResizeDialog(QWidget *parent) :
    QDialog(parent),
    ui(new Ui::FResizeDialog)
{
    ui->setupUi(this);
    vX = 640;
    vY = 480;
}

/*****************************************************************************
 *
 *****************************************************************************/
FResizeDialog::~FResizeDialog()
{
    delete ui;
}

/*****************************************************************************
 *
 *****************************************************************************/
int FResizeDialog::getX()
{
    return vX;
}

/*****************************************************************************
 *
 *****************************************************************************/
int FResizeDialog::getY()
{
    return vY;
}

/*****************************************************************************
 *
 *****************************************************************************/
void FResizeDialog::setX(int x)
{
    vX = x;
}

/*****************************************************************************
 *
 *****************************************************************************/
void FResizeDialog::setY(int y)
{
    vY = y;
}

/*****************************************************************************
 *
 *****************************************************************************/
void FResizeDialog::showEvent(QShowEvent*)
{
    ui->spnX->setValue(vX);
    ui->spnY->setValue(vY);
}

/*****************************************************************************
 *
 *****************************************************************************/
void FResizeDialog::on_cmdOk_clicked()
{
    vX = ui->spnX->value();
    vY = ui->spnY->value();
    close();
}

/*****************************************************************************
 *
 *****************************************************************************/
void FResizeDialog::on_cmdCancel_clicked()
{
    ui->spnX->setValue(vX);
    ui->spnY->setValue(vY);
    close();
}
