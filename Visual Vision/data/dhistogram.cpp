#include "dhistogram.h"

/*****************************************************************************
 *
 *****************************************************************************/
DHistogram::DHistogram()
{
}

/*****************************************************************************
 *
 *****************************************************************************/
DHistogram::~DHistogram()
{

}

/*****************************************************************************
 *
 *****************************************************************************/
DatType DHistogram::getType()
{
    return HISTOGRAM;
}

/*****************************************************************************
 *
 *****************************************************************************/
void DHistogram::setData(QVector<int> val)
{
    dat = val;
}

/*****************************************************************************
 *
 *****************************************************************************/
QVector<int> DHistogram::getData()
{
    return dat;
}

/*****************************************************************************
 *
 *****************************************************************************/
int DHistogram::getMaxValue()
{
    int max = 0;
    for (int i = 0; i < dat.size(); i++)
    {
        if (dat[i] > max)
        {
            max = dat[i];
        }
    }
    return max;
}
